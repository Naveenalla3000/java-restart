import java.lang.*;
import java.util.*;

public class Sample {
    public static void main(String[] args) {

        int  a= 32;
        float b = 32.5f;
        double c = 35.5;
        long d = 12345l;

        System.out.println("Number: " + a);
        System.out.println("Float: " + b);
        System.out.println("Double: " + c);
        System.out.println("Long: " + d);

        char ch = 'n';
        System.out.println("Char: " + ch);


        String name = "Naveen";
        System.out.println("Name: " + name);
    }    
}
